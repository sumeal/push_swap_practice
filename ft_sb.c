#include "ft_push_swap.h"

void	ft_sb(t_stack *b, int i)
{
	t_stack	*tmp;

	if (!*b || !((*b)->next))
		return ;
	tmp = *b;
	*b = (*b)->next;
	tmp->next = (*b)->next;
	(*b)->next = tmp;
	if (i ==0)
		write(1, "sb\n", 3);
}