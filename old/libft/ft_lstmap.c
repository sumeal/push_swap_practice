#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*temp;
	t_list	*new;
	t_list	*list;

	temp = NULL;
	list = NULL;
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&list, del);
			return (NULL);
		}
		if (!list)
			list = new;
		else
			temp->next = new;
		temp = new;
		lst = lst->next;
	}
	return (list);
}
